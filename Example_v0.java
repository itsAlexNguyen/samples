/**
Class A that supports adding and subtracting
*/
public class A {
    private static int X;
    private static int Y = 5;

    public A(){
        A.X = 5;
    }
    
    /**Javadoc
    Multiplication of field X and Y
    @return int multiplication result
    */
    public int fcn(){
        return A.X * A.Y;
    }
    
    public int test(){
        return A.X * A.Y;
    }
    
    public int test(){
        return 25 + 31;
    }
    
    public int test3(){
        return 999 - 25;
    }
    
    public int test5(){
        return 999 - 25;
    }
}
